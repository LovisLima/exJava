
public class Cartao {
	
	public int Id;
	public String OwnerName;
	public double Valor;
	
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getOwnerName() {
		return OwnerName;
	}
	public void setOwnerName(String ownerName) {
		OwnerName = ownerName;
	}
	public double getValor() {
		return Valor;
	}
	public void setValor(double valor) {
		this.Valor = valor;
	}
	
	
	
	
}
