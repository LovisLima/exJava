public class Aluno {

private long ra;
private String nome;
private String cidade;


public long getRa() {
	return ra;
}

public String getNome() {
	return nome;
}

public String getCidade() {
	return cidade;
}

public void setRa(long ra) {
	this.ra = ra;
}

public void setNome(String nome) {
	this.nome = nome;
}

public void setCidade(String cidade) {
	this.cidade = cidade;
}


}
